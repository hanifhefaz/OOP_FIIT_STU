    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        DBConnection con = new DBConnection(LMS);
        int borrowerId = Integer.valueOf(jTextField1.getText());
        int bookId = Integer.valueOf(jTextField2.getText());
        Borrower borrower = con.getBorrower(borrowerId);
        Book book = con.library.findBook(bookId);
        if (book == null) {
            jTextArea1.setText("No Book Found!");
            return;
        }
        else if (book.getIssueStatus() == false ) {
            jTextArea1.setText("Book not Issued Yet!");
            return;
        }
        Loan loan = con.library.findLoan(bookId);
        book.checkInThisBook(borrower, loan, (Members)type);
        con.checkThisBookInSQL(borrower, loan, (Members)type);
        jTextArea1.setText("Successfully Completed the Check In Process.");
    }//GEN-LAST:event_jButton1MouseClicked

