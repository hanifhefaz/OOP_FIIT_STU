    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        // TODO add your handling code here:
        int id = Integer.valueOf(bookIdTF.getText());
        String title = titleTF.getText();
        String author = authorTF.getText();
        String subj = subjTF.getText();
        if (title.equals("") || authorTF.equals("") || subjTF.equals("")) {
            JOptionPane.showMessageDialog(null, "All Fields are Required."); 
            return;
        }
        DBConnection con = new DBConnection(LMS);
        int status = con.updateBookInLibrary(id, title, author, subj); //also adds in DB
        if (status == 2) {
            jTextArea1.setText("Successfully updated a Book's Information!");
            con.updateBookInSQL(id, title, author, subj);
        }
        else if (status == 1) {
            jTextArea1.setText("No Book Found!");
        }
        else
        {
            jTextArea1.setText("*** ERROR ****!");
        }
    }//GEN-LAST:event_updateBtnMouseClicked

