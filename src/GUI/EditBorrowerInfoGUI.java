    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        // TODO add your handling code here:
        int id = Integer.valueOf(borrowerIdTF.getText());
        String name = nameTF.getText();
        String addr = addrTF.getText();
        String phone = phoneTF.getText();
         if (name.equals("") || addr.equals("") || phone.equals("")) {
            JOptionPane.showMessageDialog(null, "All Fields are Required."); 
            return;
        }
        DBConnection con = new DBConnection(LMS);
        int status = con.updateBorrowerInLibrary(id, name, addr, phone); //also adds in DB
        if (status == 2) {
            jTextArea1.setText("Successfully updated a Borrower's Information!");
            con.updateBorrowerInSQL(id, name, addr, phone);
        }
        else if (status == 1) {
            jTextArea1.setText("No Borrower Found!");
        }
        else
        {
            jTextArea1.setText("*** ERROR ****!");
        }
    }//GEN-LAST:event_updateBtnMouseClicked

