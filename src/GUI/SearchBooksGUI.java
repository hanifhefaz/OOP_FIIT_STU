    private void searchBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchBtnMouseClicked
        // TODO add your handling code here:
        DBConnection con = new DBConnection(LMS);
        String txtFValue = jTextField1.getText();
        List<Book> books = new ArrayList<>();
        if (authorRBtn.isSelected()) 
        {
            books = con.searchBooksByAuthor(txtFValue);
        }
        else if (subjectRBtn.isSelected()) 
        {
            books = con.searchBooksBySubject(txtFValue);
        }
        else if (titleRBtn.isSelected()) 
        {
            books = con.searchBooksByTitle(txtFValue);
        }
        else
        {
            jTextArea1.setText("Nothing Found!");
        }
        jTextArea1.setText("Available Books are:");
        for (Book book : books) {
            jTextArea1.append("\n" + "Book Title: " + book.getTitle() + "    Subject: " + book.getSubject() + "    Author:" + book.getAuthor());
        }
        
    }//GEN-LAST:event_searchBtnMouseClicked

